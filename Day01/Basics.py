# Day 1: Getting Started with Python 

# What is Python?

# - Python is a powerful and versatile programming language.
# - It's beginner-friendly and widely used for web development, data analysis, artificial intelligence, and more.

# Setting Up Python

## Install Python:
# - Visit python.org and download the latest version.
# - Follow the installation instructions for your operating system.

## Check Python Installation:

# - Open your terminal (Command Prompt or PowerShell on Windows, Terminal on macOS/Linux).
# - Type python --version or python -V and press Enter. You should see the installed Python version.

# Writing Your First Python Code

## Open a Text Editor:
# - Use a simple text editor like VSCode, Atom, or Sublime Text.

## Write Your Code:

# - Create a new file, e.g., hello.py.
# - Type:

print("Hello, Python!")

## Run Your Code:
# - Save the file.
# - Open the terminal and navigate to the file's directory.
# - Type python hello.py and press Enter.
# - You should see "Hello, Python!" printed on the screen.

# Understanding the Code
# - print(): A function to display text or variables.
# - "Hello, Python!": A string, which is text enclosed in double quotes.

# Key Concepts
## Comments:
# - Use # to add comments explaining your code. Comments are ignored by Python.
#   ```python
#   # This is a comment
#   print("Hello, Python!")
#   ```

## Variables:
# - Store data in variables.
#   ```python
#   message = "Hello, Python!"
#   print(message)
#   ```

## Indentation:
# - Python uses indentation to define code blocks. Be consistent with spaces or tabs.
#   ```python
#   if True:
#       print("Indented code")
#   ```

# Next Steps

# Strings 

# Strings are sequences of characters in Python, used to represent text.

# String Operations
greeting = "Hello"
name = "Alice"

# Concatenation
full_greeting = greeting + ", " + name + "!"
print(full_greeting)

# String Length
length = len(full_greeting)
print("Length of the greeting:", length)

# Accessing Characters
first_char = full_greeting[0]
last_char = full_greeting[-1]
print("First character:", first_char)
print("Last character:", last_char)